n = int(input("Digite um número: "))
if n == 1:
    print("Corresponde a um domingo.")
elif n == 2:
    print("Corresponde a uma segunda.")
elif n == 3:
    print("Corresponde a uma terça.")
elif n == 4:
    print("Corresponde a uma quarta.")
elif n == 5:
    print("Corresponde a uma quinta.")
elif n == 6:
    print("Corresponde a uma sexta.")
elif n == 7:
    print("Corresponde a um sábado.")
else:
    print("Não há dias correspondentes.")